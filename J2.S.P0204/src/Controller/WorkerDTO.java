package Controller;

/**
 *
 * @author chiss
 */
public class WorkerDTO {
    
    private String name;
    private String gender;
    private String role;
    private String image;

    public WorkerDTO() {
    }

    public WorkerDTO(String name, String gender, String role, String image) {
        this.name = name;
        this.gender = gender;
        this.role = role;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
    
    
}
